#include <stdio.h>
#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	a;

	a = ft_strlen(s);
	while (a >= 0)
	{
		if ((char)c == *(s + a))
			return ((char *)(s + a));
		a--;
	}
	return (0);
}
